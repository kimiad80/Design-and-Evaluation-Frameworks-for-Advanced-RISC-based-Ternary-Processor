module TIM (
		input logic clk,
		input logic [17:0] address,
		
		output logic [17:0] instruction
	);
	
	logic [17:0] inst_mem [1023:0];
	
	initial begin
		inst_mem[0]  = 18'b10_00_01_01001010_00_01; // LUI R5 (1+4)             ---> R5 = 01_00_10_10_00_00_00_00_00
		inst_mem[1]  = 18'b10_01_0100100110_00_01;  // LI R5 (1+4)              ---> R5 = 01_00_10_10_01_00_10_01_10
		inst_mem[2]  = 18'b01_01_000001_00_00_00_01; // STORE TDM[0+1] = R5     ---> TDM[1] = 01_00_10_10_01_00_10_01_10
		inst_mem[3]  = 18'b10_01_1000010010_01_10; // LI R6 (2+4)               ---> R6 = 00_00_00_00_10_00_01_00_10
		inst_mem[4]  = 18'b10_00_00_00_00_0001_00_01; // SLI R5 (1)             ---> R5 = 00_10_10_01_00_10_01_10_00
		inst_mem[5]  = 18'b10_00_00_10_00_0001_01_10; // SRI R6 (1)             ---> R6 = 00_00_00_00_00_10_00_01_00
		inst_mem[6]  = 18'b01_00_000001_00_00_00_10; // LOAD R3 (4-1) = TDM[1]  ---> R3 = 01_00_10_10_01_00_10_01_10
		inst_mem[7]  = 18'b10_10_10_01_10_01_10_00_01; // SR R5 >> R6[1:0]      ---> R5 = 00_00_00_00_10_10_01_00_10
		inst_mem[8]  = 18'b10_10_00_10_10_00_10_10_01; // MV R2 <- R3           ---> R2 = 01_00_10_10_01_00_10_01_10
		inst_mem[9]  = 18'b10_10_10_10_10_10_01_10_00; // PTI R1 = PTI(R2)      ---> R1 = 10_01_01_01_10_01_01_10_01
		inst_mem[10] = 18'b10_10_10_10_00_10_01_00_10; // NTI R3 = NTI(R2)      ---> R3 = 10_10_01_01_10_10_01_10_01
		inst_mem[11] = 18'b10_10_10_10_01_10_01_10_01; // STI R2 = STI(R2)      ---> R2 = 10_00_01_01_10_00_01_10_01
		inst_mem[12] = 18'b10_10_00_10_00_01_10_00_01; // ADD R5 = R5+R6        ---> R5 = 00_00_00_10_01_01_01_01_10
		inst_mem[13] = 18'b10_10_00_10_01_01_10_00_01; // SUB R5 = R5-R6        ---> R5 = 00_00_00_00_10_10_01_00_10
		inst_mem[14] = 18'b10_00_10_10_100110_00_01; // ANDI R5 & 13            ---> R5 = 00_00_00_00_10_10_10_00_10
		inst_mem[15] = 18'b10_00_00_00_00_0101_01_10; // SLI R6 (4)             ---> R6 = 00_10_00_01_00_00_00_00_00
		inst_mem[16] = 18'b10_01_0101100010_01_10; // LI R6                     ---> R6 = 00_10_00_01_01_01_10_00_10
		inst_mem[17] = 18'b10_00_00_00_00_0100_00_01; // SLI R5 (3)             ---> R5 = 00_10_10_10_00_10_00_00_00
		inst_mem[18] = 18'b10_01_0001001001_00_01; // LI R5                     ---> R5 = 00_10_10_10_00_01_00_10_01
		inst_mem[19] = 18'b10_10_00_10_10_00_01_01_01; // MV R8 <- R5           ---> R8 = 00_10_10_10_00_01_00_10_01
		inst_mem[20] = 18'b10_10_10_00_10_01_10_01_01; // AND R8 = R8&R6        ---> R8 = 00_10_10_10_00_01_10_10_10
		inst_mem[21] = 18'b10_10_00_10_10_00_01_01_00; // MV R7 <- R5           ---> R7 = 00_10_10_10_00_01_00_10_01
		inst_mem[22] = 18'b10_10_10_00_00_01_10_01_00; // OR R7 = R7|R6         ---> R7 = 00_10_00_01_01_01_00_00_01
		inst_mem[23] = 18'b10_10_10_00_01_00_01_01_10; // XOR R6 = R5^R6        ---> R6 = 00_10_00_01_00_10_00_00_01
		inst_mem[24] = 18'b10_10_10_01_00_01_10_00_01; // SL R5 << R6[1:0]      ---> R5 = 10_10_10_00_01_00_10_01_00
		
		inst_mem[25] = 18'b10_10_10_01_01_00_00_00_01; // COMP
		inst_mem[26] = 18'b00_10_00000110_00_00_01; // BEQ R5[0] == B           ---> Jumps to inst_mem[28] (Branch taken)
		inst_mem[27] = 18'b01_01_000110_00_00_01_01; // TDM[2] = R8             ---> Not performed
		inst_mem[28] = 18'b01_01_000100_00_00_01_01; // TDM[3] = R8             ---> TDM[3] = 00_10_10_10_00_01_10_10_10
		
		inst_mem[29] = 18'b10_10_10_01_01_00_00_00_01; // COMP
		inst_mem[30] = 18'b00_00_00000100_01_00_01; // BNE R5[0] != B           ---> Jumps to inst_mem[33] (Branch taken)
		inst_mem[31] = 18'b01_01_000110_00_00_01_00; // TDM[2] = R7             ---> Not performed
		inst_mem[32] = 18'b10_00_10_00_00_00_00_10_10; // NOP
		inst_mem[33] = 18'b01_01_000101_00_00_01_00; // TDM[4] = R7             ---> TDM[4] = 00_10_00_01_01_01_00_00_01
		
		inst_mem[34] = 18'b10_10_10_01_01_00_00_00_01; // COMP
		inst_mem[35] = 18'b00_10_00000110_01_00_01; // BEQ R5[0] == B           ---> Doesn't jump to inst_mem[37] (Branch not taken)
		inst_mem[36] = 18'b10_10_10_01_01_00_00_00_01; // COMP
		inst_mem[37] = 18'b00_00_00000110_00_00_01; // BNE R5[0] != B           ---> Doesn't jump to inst_mem[39] (Branch not taken)
		inst_mem[38] = 18'b01_01_011010_00_00_01_10; // TDM[5] = R6             ---> TDM[5] = 00_10_00_01_00_10_00_00_01
		
		inst_mem[39] = 18'b00_01_0000000101_00_01; // JAL R5 = 40, PC = 43      ---> R5 = 10_10_10_10_10_00_00_00_00 , PC = 10_10_10_10_10_00_00_01_00
		
		inst_mem[43] = 18'b01_10_010010_00_01_01_10; // JALR R6 = 44, PC = R5+8 ---> R6 = 10_10_10_10_10_00_00_01_01 , PC = 10_10_10_10_10_00_01_00_10
		
		// The following two instructions are written to examine Hazard Detection Unit's stall control (read after load)
		inst_mem[48] = 18'b01_00_000001_00_00_00_00; // LOAD R4 = TDM[1]        ---> R4 = 01_00_10_10_01_00_10_01_10
		inst_mem[49] = 18'b10_00_00_10_00_01_00_00_00; // SRI R4 (3)            ---> R4 = 00_00_00_01_00_10_10_01_00
		
	end
	
	logic [31:0] decimal;
	integer i;
	logic signed [31:0] weighted_value;
	logic signed [1:0] trit_value;
	
	always_comb begin
		decimal = 32'd9841; // 3**8 + 3**7 + 3**6 + 3**5 + 3**4 + 3**3 + 3**2 + 3**1 + 3**0 = 9841
		for (i = 0; i < 9; i = i + 1) begin
			case (address[2*i +: 2])
				2'b00: trit_value = 0;
				2'b01: trit_value = 1;
				2'b10: trit_value = -1;
				default: trit_value = 0;
			endcase

			weighted_value = trit_value * (3 ** i);
			decimal = decimal + weighted_value;
		end
	end

	// Synchronous Read
	always_ff @(posedge clk) begin
		instruction <= inst_mem[decimal];
	end
	
endmodule